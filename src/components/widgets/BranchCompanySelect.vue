<template>
    <el-select size="mini"  v-model="currentValue" placeholder="请选择" remote :remote-method="loadData" :loading="loading" :clearable="clearable" :disabled="disabled"
                @change="handleChange"  filterable>
        <el-option v-for="item in options" :key="item.id" :label="item.name" :value="item.id" :disabled="item.disabled">
        </el-option>
    </el-select>
</template>

<script>

  export default {
    data () {
      return {
        loading: false,
        options: [],
        disOpts: []
      }
    },
    props: {
      value: {
        required: true
      },
      clearable: {
        type: Boolean
      },
      disabled: {
        type: Boolean
      },
      emptyLabel: {
        required: false
      },
      lockerIds: {
      }
    },
    computed: {
      currentValue: {
        get () {
          return this.value
        },
        set (val) {
          this.$emit('input', val)
        }
      }
    },
    watch: {
    },
    methods: {
      handleChange (val) {
        this.$emit('change', val)
      },
        loadData (query) {
            query = (query || '').replace(/\[.*\]/g, '')
            query = query.replace(/^\s+|\s+$/gm, '')
            if (query) {
                this.loading = true
                this.$api.ipark.BranchCompanyService.query({param: {name: query, isDeleted: false}}).then(rsp => {
                    this.options = rsp.data
                    this.loading = false
                })
            } else {
                this.options = []
                this.refresh()
            }
        },
      refresh (options) {
        this.loading = true
        this.$api.ipark.BranchCompanyService.query({
            limit:100,
            param: {isDeleted: false}
        }).then(rsp => {
            this.options = rsp.data
            this.loading = false
        })
      }
    },
    mounted() {
       this.refresh();
    }

  }
</script>
